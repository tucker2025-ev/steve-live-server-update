                                         service_fee_per_kwh DECIMAL(10,2),
                                         service_fee_amount DECIMAL(10,2),
                                         session_amount DECIMAL(10,2),
                                         session_total_amount DECIMAL(10,2),
                                         settlement_amount_owner_with_gst_client DECIMAL(10,2),
                                         settlement_amount_owner_non_gst_client DECIMAL(10,2),
                                         total_amount_owner_with_gst DECIMAL(12,2),
                                         total_amount_owner_non_gst DECIMAL(12,2),
                                         session_start_at TIMESTAMP(6),
                                         session_end_at TIMESTAMP(6),
                                         session_status TINYINT(1)
);